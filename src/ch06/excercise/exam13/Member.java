package ch06.excercise.exam13;

public class Member {
	private String name;
	private String id;
	private String password;
	private int age;
	private boolean married;
	
	public boolean isMarried() {
		return married;
	}
	
	public String setName() {
		return name;
	}
	
	public String setId() {
		return id;
	}
	
	public String setPassword() {
		return password;
	}
	
	public int setAge() {
		return age;
	}
}
